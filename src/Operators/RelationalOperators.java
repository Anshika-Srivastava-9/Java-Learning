package Operators;

public class RelationalOperators {
    public static void main(String[] args) {
        int p = 3;
        int q = 7;
        System.out.println(p==q);
        System.out.println(p!=q);
        System.out.println(p>=q);
        System.out.println(p<=q);
        System.out.println(p<q);
        System.out.println(p>q);
        }
    }
